from sklearn.cluster import KMeans
from sklearn.datasets import load_digits
from sklearn.metrics import silhouette_score


ld = load_digits()
print(ld.target[:,20])
km = KMeans(n_clusters=810)
km.fit(ld.data)
print(km.labels_[:,20])
print(silhouette_score(ld.data,km.labels_))