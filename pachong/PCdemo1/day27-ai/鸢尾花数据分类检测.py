# 加载鸢尾花
from sklearn.datasets import load_iris
# K近邻分类器
from sklearn.neighbors import KNeighborsClassifier
data_iris = load_iris()
print(data_iris.data)
print(data_iris.target)

#
knn = KNeighborsClassifier()
#训练模型
knn.fit(data_iris.data,data_iris.target)
print(data_iris.target_names)
# 预测
new_data = [[4.1,3.2,1.3,0.4]]

result = knn.predict(new_data)


print(result)
print(data_iris.target_names[result[0]])

